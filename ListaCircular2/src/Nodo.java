// Clase Nodo
public class Nodo {
int valor;  // El valor que guarda el nodo (un número entero)
Nodo siguiente;  // Apunta al siguiente nodo en la lista

// Constructor para crear un nodo con un valor
public Nodo(int valor) {
this.valor = valor;
this.siguiente = null;  // Inicialmente, no apunta a ningún nodo
}
}
